package com.agifans.picedit;

/**
 * This enum represents the two different variations of the Step tool, 
 * i.e. what I originally called in my AGI SPECS PICTURE format docs
 * as the x-corner and y-corner. Sierra On-Line called them horizontal
 * and vertical, depending on which direction it goes on the first step. 
 * I think both descriptions are misleading but I'll stick with what I 
 * called it 13 years ago.
 * 
 * @author Lance Ewing
 */
public enum StepType {

    XCORNER,
    YCORNER;
}
